// react provides multiple ways to achieve conditional rendering, depending on the complexity and requirements of your application.
// પ્રતિક્રિયા તમારી એપ્લિકેશનની જટિલતા અને આવશ્યકતાઓને આધારે શરતી રેન્ડરિંગ હાંસલ કરવાની બહુવિધ રીતો પ્રદાન કરે છે.



// if-else statements :-  you can use js if-else statement inside the render() method of a component to conditionally render different content 
// if-else સ્ટેટમેન્ટ્સ :- તમે અલગ કન્ટેન્ટને શરતી રીતે રેન્ડર કરવા માટે ઘટકની રેન્ડર() પદ્ધતિની અંદર js if-else સ્ટેટમેન્ટનો ઉપયોગ કરી શકો છો. 

// ternary operators :- you can use the ternary operator (? :) inside curly braces {} to conditionally render content within jsx.
// ટર્નરી ઓપરેટર્સ :- jsx ની અંદર કન્ટેન્ટને શરતી રીતે રેન્ડર કરવા માટે તમે કર્લી કૌંસની અંદર ટર્નરી ઓપરેટર (? :) નો ઉપયોગ કરી શકો છો.

// logical && operators :- when the condition is true, the components is rendered; otherwise, it is skipped. this is particularly useful when you want to render nothing or a default state when a condition is false.
// લોજિકલ && ઓપરેટર્સ :- જ્યારે શરત સાચી હોય, ત્યારે ઘટકો રેન્ડર કરવામાં આવે છે; અન્યથા, તે છોડવામાં આવે છે. આ ખાસ કરીને ઉપયોગી છે જ્યારે તમે કંઈપણ અથવા ડિફોલ્ટ સ્થિતિને રેન્ડર કરવા માંગતા નથી જ્યારે શરત ખોટી હોય.

// conditional render with components :- in more complex scenarios, you may want to render different components based on conditions.
